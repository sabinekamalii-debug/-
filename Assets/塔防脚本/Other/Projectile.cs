using UnityEngine;

public class Projectile : MonoBehaviour
{
    private Transform target;
    public float speed = 20f;
    private int damage;
    private bool ignoreDefense;
    private bool hasHit = false;

    public void Seek(Transform _target, int _damage, bool _ignoreDefense = false)
    {
        target = _target;
        damage = _damage;
        ignoreDefense = _ignoreDefense;
    }

    void Update()
    {
        if (target == null)
        {
            Destroy(gameObject);
            return;
        }

        // --- ???????? 1??????????? Z ?????? 0 ---
        // ???????????????�??????????????????????
        Vector3 currentPos = transform.position;
        currentPos.z = 0f;
        transform.position = currentPos;

        // --- ???????? 2??????????�?????????????? Z ?? ---
        // ???�???????? (Z=1000)?????????????? (X, Y, 0)
        Vector3 targetPos = target.position;
        targetPos.z = 0f;

        // --- 3. ?????? (??????? 2D ?????) ---
        float step = speed * Time.deltaTime;
        transform.position = Vector3.MoveTowards(transform.position, targetPos, step);

        // --- 4. ?????? ---
        Vector3 dir = targetPos - transform.position;
        if (dir != Vector3.zero)
        {
            // Atan2 ????? Y ?? X?????????? 2D ???????
            float angle = Mathf.Atan2(dir.y, dir.x) * Mathf.Rad2Deg;
            transform.rotation = Quaternion.AngleAxis(angle, Vector3.forward);
        }

        // --- 5. ?????? ---
        // ??????? Z=0 ??????????�?????
        float distance = Vector3.Distance(transform.position, targetPos);
        if (distance < 0.1f) // ????????????????????????
        {
            HitTarget();
        }
    }

    void HitTarget()
    {
        if (hasHit) return;
        hasHit = true;
        // --- ????????? ---

        // 1. ???????????
        Enemy2 enemy = target.GetComponent<Enemy2>();
        if (enemy == null) enemy = target.GetComponentInParent<Enemy2>();

        // 2. ???????????? (?�??)
        SpawnerHealth spawner = target.GetComponent<SpawnerHealth>();

        // 3. ???�???? (������Ѫ)
        OperatorUnit op = target.GetComponent<OperatorUnit>();
        if (op == null) op = target.GetComponentInParent<OperatorUnit>();

        // 4. ??????
        if (enemy != null)
        {
            enemy.TakeDamage(damage, ignoreDefense);
        }
        else if (spawner != null)
        {
            spawner.TakeDamage(damage);
        }
        else if (op != null)
        {
            op.TakeDamage(damage, ignoreDefense);
        }
        else { }
        // --- ?????????? ---

        Destroy(gameObject);
    }
}